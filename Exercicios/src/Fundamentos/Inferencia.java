package Fundamentos;

public class Inferencia {
	
	public static void main(String[] args) {
		
		double a; // Variavel foi declarada  
		a = 4.5; // Variavel foi inicializada
		System.out.println(a); // usada
		
		var b = 4.5; // declaração e inicialização obrigatoria na mesma linha para var 
		System.out.println(b);
		
		var c = "Maria Silva"; // Uma vez definida o tipo não é possivel alterar a menos que se encaixe como Real para Inteiro
		System.out.println(c);
		
		var d = "Francisco";
		System.out.println(d);

	
		
	}

}
